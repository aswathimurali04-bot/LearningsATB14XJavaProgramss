package Ex_04_Operators;

public class Lab0018_Unary_Operators {
    public static void main(String[] args) {
        int guide=-120;
        int buide=700;

        System.out.println(guide-buide);
        System.out.println(guide+buide);

        int a = +10;
        int a1 = -110;
        int result = a+a1;
        System.out.println(result);

        //Arithmetic operation (b+1)
        int b = -1;
        b = b+1;
        System.out.println(b);

        //Direct assignment (12)
        int h=10;
        h=12;
        System.out.println(h);

    }
}
