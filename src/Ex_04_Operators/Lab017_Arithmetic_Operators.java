package Ex_04_Operators;

public class Lab017_Arithmetic_Operators {
    public static void main(String[] args) {


        int g=234;
        float f=56.8f;
        double b=56.0d;

        System.out.println("The Result is:" + g+f+b);
        System.out.println(g-f);
        System.out.println(g*f*b);
        System.out.println(g/f);




    }
}
